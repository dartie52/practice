# Повертає:
#   list: список кортежів, кожен з яких містить трійку чисел (a, b, c), що відповідають умові.
# Обмеження:
#   Функція перевіряє лише додатні цілі значення від 1 до n.
#   Не включає від’ємні значення або нуль.
# Можливі помилки:
#   Якщо n не є цілим додатнім числом, функція працюватиме некоректно або виведе порожній список.
def find_pythagorean_triples(n): # Функція призначена для пошуку всіх трійок цілих чисел (a, b, c)
    # Ініціалізація порожнього списку для збереження знайдених трійок.
    triples = []
    for a in range(1, n + 1): # Цикл перебирає всі можливі значення a 
        for b in range(a, n + 1):  # Вкладений цикл перебирає всі можливі значення b від a до n включно.
            for c in range(b, n + 1):  # Ще один цикл перебирає всі можливі значення c від b до n включно.
                # Якщо виконано рівність a^2 + b^2 == c^2, додаємо трійку до списку.
                if a * a + b * b == c * c:   # Якщо виконано рівність a^2 + b^2 == c^2 
                    triples.append((a, b, c)) # то додаємо трійку до списку.
    return triples  # Повертаємо список усіх знайдених трійок.

# Приклад виклику функції: знайти всі трійки до 30
result = find_pythagorean_triples(30)
print(result)  # Очікувано: [(3, 4, 5), (5, 12, 13), (6, 8, 10), ...] тощо
